# Spatie Guidelines
- **DTO**: `spatie/laravel-data`.
- **Logs**: `activitylog` (Business), `auditing` (Compliance).
- **Filament**: Official plugins MANDATORY.
- **Webhooks**: `webhook-client` (Idempotent Job).
- **Media**: `medialibrary`.
- **Settings**: `laravel-settings`.
- **Tags**: `laravel-tags`.
- **States**: `model-states`.